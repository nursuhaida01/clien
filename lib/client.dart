  Future<void> connect() async {
    if (isConnected) {
      debugPrint("‚úÖ ‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡πÑ‡∏õ‡πÅ‡∏•‡πâ‡∏ß: $hostname:$port");
